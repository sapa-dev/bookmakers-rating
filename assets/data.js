// Функция-имитатор fetch-запроса
function fakeFetch(url) {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            try {
                const parsedUrl = new URL(url, window.location.origin);
                const path = parsedUrl.pathname.replace(/^\/+|\/+$/g, ''); // без слэшей
                const searchParams = Object.fromEntries(parsedUrl.searchParams.entries());

                if (typeof window[path] !== 'function') {
                    throw new Error(`Функция "${path}" не найдена`);
                }

                const result = window[path](searchParams);
                resolve(result);
            } catch (error) {
                reject(error.message);
            }
        }, 300);
    });
}

